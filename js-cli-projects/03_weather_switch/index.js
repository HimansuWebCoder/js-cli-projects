// Mini Weather switch app 
let weatherName = "rainy";

switch (weatherName) {
case "sunny":
	console.log(`This is ${weatherName}`);
	break;
case "cloudy":
	console.log(`This is ${weatherName}`);
	break;
case "rainy": 
	console.log(`This is ${weatherName}`);
	break;
case "snowy": 
	console.log(`This is ${weatherName}`);
	break;
default: 
	console.log(`This is ${weatherName}`);		
	break;
}